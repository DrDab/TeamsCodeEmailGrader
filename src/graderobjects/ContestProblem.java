package graderobjects;

public class ContestProblem
{
    public String name;
    public String i1;
    public String i2;
    public String i3;
    public String o1;
    public String o2;
    public String o3;

    public ContestProblem(String name, String i1, String i2, String i3, String o1, String o2, String o3)
    {
        this.name = name;
        this.i1 = i1;
        this.i2 = i2;
        this.i3 = i3;
        this.o1 = o1;
        this.o2 = o2;
        this.o3 = o3;
    }
}
